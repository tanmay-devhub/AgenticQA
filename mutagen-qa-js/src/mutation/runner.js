/**
 * Stryker JS runner.
 *
 * Writes a stryker.conf.mjs into the workdir, spawns Stryker using the local
 * @stryker-mutator/core install, then parses reports/mutation/mutation.json
 * and returns a MutationReport matching the Python side's JSON shape.
 *
 * Assumptions:
 *   - @stryker-mutator/core is installed in mutagen-qa-js/node_modules.
 *   - Tests use node's built-in test runner (node --test), invoked via
 *     Stryker's `command` runner. Simple, no jest peer dep required.
 */

import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { runSubprocess } from "../sandbox/executor.js";
import { makeMutationReport } from "./report.js";

const _thisDir = path.dirname(fileURLToPath(import.meta.url));
// mutagen-qa-js package root: two levels up from src/mutation.
const PKG_ROOT = path.resolve(_thisDir, "..", "..");

// Drift guard against the shared mutation-kind taxonomy. If Stryker gains a
// new mutator we forget to map, the fallback is "other" -- but if we ever
// invent a kind here that the schema doesn't declare, the classifier and
// planner will disagree with the Python side. Fail import loud in that case.
const _SCHEMA_PATH = path.resolve(PKG_ROOT, "..", "shared", "schema", "mutation_kinds.json");
const _SCHEMA_KINDS = new Set(JSON.parse(readFileSync(_SCHEMA_PATH, "utf8")).kinds);

// Coarse mapping from Stryker mutator names to our cross-language kind
// taxonomy. Anything not listed falls through to "other".
const MUTATOR_KIND = {
  EqualityOperator: "comparison",
  ConditionalExpression: "boolean",
  LogicalOperator: "boolean",
  ArithmeticOperator: "arithmetic",
  UpdateOperator: "arithmetic",
  AssignmentOperator: "arithmetic",
  UnaryOperator: "arithmetic",
  BooleanLiteral: "constant",
  StringLiteral: "constant",
  ArrayDeclaration: "constant",
  ObjectLiteral: "constant",
  Regex: "constant",
  BlockStatement: "other",
  MethodExpression: "call",
  OptionalChaining: "call",
};

function strykerScript() {
  // Spawn the raw .js entry via `node <path>` rather than the .cmd/.sh shim.
  // Windows child_process.spawn rejects .cmd files with EINVAL unless
  // shell:true is used; skipping the shim keeps the call uniform across OSes.
  const script = path.join(PKG_ROOT, "node_modules", "@stryker-mutator", "core", "bin", "stryker.js");
  return existsSync(script) ? script : null;
}

export function writeStrykerConfig(workdir, { targetFile, testFiles, disabledMutators = [], timeoutMs = 5000 }) {
  const testCmd = ["node", "--test", ...testFiles].join(" ");
  const cfg = `// Auto-generated by mutagen-qa-js. Do not edit by hand.
export default {
  mutate: ${JSON.stringify([targetFile])},
  testRunner: "command",
  commandRunner: { command: ${JSON.stringify(testCmd)} },
  timeoutMS: ${timeoutMs},
  concurrency: 1,
  coverageAnalysis: "off",
  disableTypeChecks: false,
  reporters: ["json"],
  jsonReporter: { fileName: "reports/mutation/mutation.json" },
  ${disabledMutators.length ? `mutator: { excludedMutations: ${JSON.stringify(disabledMutators)} },` : ""}
  cleanTempDir: true,
  tempDirName: ".stryker-tmp",
  logLevel: "warn",
};
`;
  writeFileSync(path.join(workdir, "stryker.conf.mjs"), cfg, "utf8");
}

function sliceSource(source, loc) {
  // Stryker locations are 1-based line, 0-based column, both inclusive-start
  // exclusive-end for the `end` field.
  const lines = source.split("\n");
  const { start, end } = loc;
  if (start.line === end.line) {
    return (lines[start.line - 1] || "").slice(start.column, end.column);
  }
  const first = (lines[start.line - 1] || "").slice(start.column);
  const middle = lines.slice(start.line, end.line - 1).join("\n");
  const last = (lines[end.line - 1] || "").slice(0, end.column);
  return [first, middle, last].filter((s) => s).join("\n");
}

function makeDiff(sourceFile, source, mutant) {
  const original = sliceSource(source, mutant.location);
  const replacement = mutant.replacement ?? "";
  return (
    `--- ${sourceFile}\n` +
    `+++ ${sourceFile}\n` +
    `@@ line ${mutant.location.start.line} @@\n` +
    `-${original}\n` +
    `+${replacement}\n`
  );
}

function classifyMutator(name) {
  const kind = MUTATOR_KIND[name] || "other";
  // Guard against drift: if someone adds a value to MUTATOR_KIND that isn't
  // in the shared schema, the dashboard (Python-driven) would render it as
  // an unknown row. Fail loud instead.
  if (!_SCHEMA_KINDS.has(kind)) {
    throw new Error(`mutator ${JSON.stringify(name)} maps to ${JSON.stringify(kind)} which is not in shared/schema/mutation_kinds.json`);
  }
  return kind;
}

export function parseStrykerReport(reportJson, { disabledMutators = [] } = {}) {
  // Stryker's mutation.json schema is versioned. We target v2. If Stryker
  // upgrades to a breaking v3, `files` may move or `mutants` may restructure.
  // Detect and warn loudly rather than silently returning zeros.
  const schema = reportJson.schemaVersion;
  if (schema && schema !== "1" && schema !== "2") {
    console.warn(
      `Stryker mutation.json schemaVersion=${schema} (expected 1 or 2). ` +
      `Report parsing may be incomplete; see src/mutation/runner.js.`,
    );
  }
  const files = reportJson.files || {};
  let total = 0;
  let killed = 0;
  let survived = 0;
  let timeout = 0;
  let skipped = 0;
  let suspicious = 0;
  const survivors = [];

  for (const [file, entry] of Object.entries(files)) {
    const source = entry.source || "";
    for (const m of entry.mutants || []) {
      total += 1;
      switch (m.status) {
        case "Killed":
          killed += 1;
          break;
        case "Survived":
        case "NoCoverage":
          survived += 1;
          survivors.push({
            id: String(m.id),
            file,
            line: m.location?.start?.line ?? null,
            status: "survived",
            kind: classifyMutator(m.mutatorName),
            diff: makeDiff(file, source, m),
          });
          break;
        case "Timeout":
          timeout += 1;
          break;
        case "CompileError":
        case "RuntimeError":
          suspicious += 1;
          break;
        case "Ignored":
          skipped += 1;
          break;
        default:
          // Unknown status -- treat as skipped so it doesn't skew the rate.
          skipped += 1;
      }
    }
  }

  return makeMutationReport({
    total,
    killed,
    survived,
    timeout,
    suspicious,
    skipped,
    survivors,
    disabledTypes: disabledMutators,
  });
}

export async function runStryker({
  workdir,
  targetFile,
  testFiles,
  disabledMutators = [],
  timeoutS = 180,
  perTestTimeoutMs = 5000,
}) {
  mkdirSync(path.join(workdir, "reports", "mutation"), { recursive: true });
  writeStrykerConfig(workdir, { targetFile, testFiles, disabledMutators, timeoutMs: perTestTimeoutMs });

  const script = strykerScript();
  if (!script) {
    return {
      report: null,
      runResult: {
        returncode: -1,
        stdout: "",
        stderr:
          "@stryker-mutator/core is not installed. run `npm install` inside mutagen-qa-js/ first.",
        timedOut: false,
      },
    };
  }
  const runResult = await runSubprocess([process.execPath, script, "run"], { cwd: workdir, timeoutS });

  const reportPath = path.join(workdir, "reports", "mutation", "mutation.json");
  if (!existsSync(reportPath)) {
    return { report: null, runResult };
  }
  let raw;
  try {
    raw = JSON.parse(readFileSync(reportPath, "utf8"));
  } catch {
    return { report: null, runResult };
  }
  const report = parseStrykerReport(raw, { disabledMutators });
  return { report, runResult };
}
